package javabasic;

public class Constant {
	
	public static void main(String[] args) {
		
		final int fi1;
		// System.out.println(fi1); // 상수는 초기화해야만 사용할 수 있다
		
		final int fi2 = 1;
		System.out.println(fi2);
		// fi2 = 2; // 상수는 초기화한 후에 값을 변경할 수 없다
		
		final float PI = 3.141592f; // 상수는 관례적으로 대문자로 이름을 짓는다
		
	}

}









